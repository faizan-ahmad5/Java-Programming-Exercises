//Exercise 12: Basic Conditional Logic (if-else)
//12.	Task: Write an if-else statement that checks whether a number is greater than 10.
// If it is, print “Number is greater than 10”; otherwise, print “Number is less than or equal to 10.”
//o	Expected Output:
//Number is [Condition Result]

public class Main {
    public static void main(String[] args) {
        int number = 5;
        if (number <= 10) {
            System.out.println("Number is less than or equal to 10.");
        } else {
            System.out.println("Number is greater than 10.");
        }
    }
}
