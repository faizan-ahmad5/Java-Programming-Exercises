//Exercise 13: Using Ternary Operator
//13.	Task: Use a ternary operator to check whether a boolean value isAvailable is true or false, and print the result.
//o	Expected Output:
//Availability: [true/false]

public class Main {
    public static void main(String[] args) {
        boolean isAvailable = false;
        String availabilityStatus = isAvailable ? "true" : "false";
        System.out.println("Availability: " + availabilityStatus);
    }
}
